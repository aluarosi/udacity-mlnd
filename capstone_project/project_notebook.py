    print(f'VARIANCE ANALYSIS:')
    print(f'Evaluating models {model_cases}, {repetitions} times each. {len(model_cases)*repetitions} iterations ahead.')
    
    #
    # Mutable stuff here. Be careful!
    #
    result = []
    counter = 0
    #
    # Iterate models
