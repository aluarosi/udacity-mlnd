#%%############################################################################

#
# Entropy calculation
#
def H(l):
    ''' Entropy calculation from a list of integers 
        that represent number of occurrences for each class.
    '''
    total = sum(l)

    probabilities = map(lambda x: x/total  , l)

    def plog2(x):
        if x == 0.0:
            return 0
        else:
            return (- x * log(x,2))

    informations = map(plog2 , probabilities)
    return sum(informations)

#%%############################################################################

